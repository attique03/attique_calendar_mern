function convertToActualTime(time) {
    return time === "1"
      ? "9:00AM"
      : time === "2"
      ? "9:30AM"
      : time === "3"
      ? "10:00AM"
      : time === "4"
      ? "10:30AM"
      : time === "5"
      ? "11:00AM"
      : time === "6"
      ? "11:30AM"
      : time === "7"
      ? "12:00PM"
      : time === "8"
      ? "12:30PM"
      : time === "9"
      ? "1:00PM"
      : time === "10"
      ? "1:30PM"
      : time === "11"
      ? "2:00PM"
      : time === "12"
      ? "2:30PM"
      : time === "13"
      ? "3:00PM"
      : time === "14"
      ? "3:30PM"
      : time === "15"
      ? "4:00PM"
      : time === "16"
      ? "4:30PM"
      : time === "17"
      ? "5:00PM"
      : time === "18"
      ? "5:30PM"
      : time === "19"
      ? "6:00PM"
      : time === "20"
      ? "6:30PM"
      : time === "21"
      ? "7:00PM"
      : time === "22"
      ? "7:30PM"
      : time === "23"
      ? "8:00PM"
      : time === "24"
      ? "8:30PM"
      : "Wrong time";
  }

  export default convertToActualTime;